using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class PlayerAttack : MonoBehaviour
{
    public Transform cam;

    public LayerMask layerPlayer;

    [SerializeField] private bool isInteract;
    [SerializeField] private float rangeInteract;
    void Start()
    {
        
    }

    // Update is called once per frame
    void Update()
    {
        RaycastHit hit;
        isInteract = Physics.Raycast(cam.transform.position, cam.TransformDirection(Vector3.forward), out hit, rangeInteract,layerPlayer);


        if(isInteract)
        {
            Debug.Log(hit.transform.name);
        }
    }
}
